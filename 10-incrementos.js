//Operadores de incremento e descremento
var idade = 16;
console.log(idade);

idade++;
console.log(idade);

idade--;
console.log(idade);

console.log("-----------------");

var novaIdade = idade++;
console.log("idade:", idade, "| nova idade:", novaIdade)
 
var novaIdade = ++idade
console.log("idade:", idade, "| nova idade:", novaIdade)

//incrementos e decremetos com valor especifico
//operadores de atribuição aritimedica
novaIdade += 10;
console.log(novaIdade)

novaIdade -= 20
console.log(novaIdade)